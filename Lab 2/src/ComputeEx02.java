
public class ComputeEx01 {

	public static void main(String[] args) {
		int a,b; //정수형 변수 a,b 선언
		int result; //정수형 변수 result 선언
		
		a = 100;
		b = 25;
		
		result = a + b;
		//sout 타이핑 후 ctrl+space 단축기 사용
		System.out.println(a+"+"+b+"="+result);
	
		result = a - b;
		//sout 타이핑 후 ctrl+space 단축기 사용
		System.out.println(a+"-"+b+"="+result);
		
		result = a * b;
		//sout 타이핑 후 ctrl+space 단축기 사용
		System.out.println(a+"*"+b+"="+result);
		
		result = a / b;
		//sout 타이핑 후 ctrl+space 단축기 사용
		System.out.println(a+"/"+b+"="+result);

	}

}
